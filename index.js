function add(x, y) {
  return x += y ;
}

function subtract(x, y) {
  return x - y ;
}

function multiply(x, y) {
  return x * y ;
}

function divide(x, y) {
  return x / y ;
}

var n = 10

function inc(n) {
  return ++n ;
}

function dec(n) {
  return --n ;
}

function makeInt(n) {
  return parseInt(n) ;
}

